        sb.appendLine("        mInstrumentation.callActivityOnCreate(activity, r.state);")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("")
        sb.appendLine("// Instrumentation.callActivityOnCreate()")
        sb.appendLine("public void callActivityOnCreate(Activity activity, Bundle icicle) {")
        sb.appendLine("    prePerformCreate(activity);")
        sb.appendLine("    activity.performCreate(icicle);")
        sb.appendLine("    postPerformCreate(activity);")
        sb.appendLine("}")
        sb.appendLine("")
        sb.appendLine("// Activity.performCreate()")
        sb.appendLine("final void performCreate(Bundle icicle, PersistableBundle persistentState) {")
        sb.appendLine("    // 分发 Fragment 事件")
        sb.appendLine("    mFragments.restoreAllState(icicle, mFragments.dispatchActivityCreated());")
        sb.appendLine("    ")
        sb.appendLine("    // 调用 onCreate")
        sb.appendLine("    if (persistentState != null) {")
        sb.appendLine("        onCreate(icicle, persistentState);")
        sb.appendLine("    } else {")
        sb.appendLine("        onCreate(icicle);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 通知 Fragment")
        sb.appendLine("    mFragments.dispatchActivityCreated();")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【onResume 调用链】\n")
        sb.appendLine("```java")
        sb.appendLine("// ActivityThread.handleResumeActivity()")
        sb.appendLine("public void handleResumeActivity(IBinder token, boolean finalStateRequest,")
        sb.appendLine("        boolean isForward, String reason) {")
        sb.appendLine("    // 调用 performResume")
        sb.appendLine("    final ActivityClientRecord r = performResumeActivity(token, finalStateRequest, reason);")
        sb.appendLine("    ")
        sb.appendLine("    if (r != null) {")
        sb.appendLine("        final Activity a = r.activity;")
        sb.appendLine("        ")
        sb.appendLine("        // 添加 DecorView 到 WindowManager")
        sb.appendLine("        if (r.window == null && !a.mFinished && willBeVisible) {")
        sb.appendLine("            r.window = r.activity.getWindow();")
        sb.appendLine("            View decor = r.window.getDecorView();")
        sb.appendLine("            ViewManager wm = a.getWindowManager();")
        sb.appendLine("            WindowManager.LayoutParams l = r.window.getAttributes();")
        sb.appendLine("            wm.addView(decor, l);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("")
        sb.appendLine("// ActivityThread.performResumeActivity()")
        sb.appendLine("public ActivityClientRecord performResumeActivity(IBinder token,")
        sb.appendLine("        boolean finalStateRequest, String reason) {")
        sb.appendLine("    ActivityClientRecord r = mActivities.get(token);")
        sb.appendLine("    r.activity.performResume();")
        sb.appendLine("    return r;")
        sb.appendLine("}")
        sb.appendLine("")
        sb.appendLine("// Activity.performResume()")
        sb.appendLine("final void performResume() {")
        sb.appendLine("    performRestart();  // 会调用 onStart()")
        sb.appendLine("    ")
        sb.appendLine("    mFragments.execPendingActions();")
        sb.appendLine("    mLastNonConfigurationInstances = null;")
        sb.appendLine("    ")
        sb.appendLine("    // 调用 onResume")
        sb.appendLine("    mInstrumentation.callActivityOnResume(this);")
        sb.appendLine("    ")
        sb.appendLine("    // 通知 Fragment")
        sb.appendLine("    mFragments.dispatchResume();")
        sb.appendLine("    mFragments.execPendingActions();")
        sb.appendLine("}")
        sb.appendLine("")
        sb.appendLine("// Instrumentation.callActivityOnResume()")
        sb.appendLine("public void callActivityOnResume(Activity activity) {")
        sb.appendLine("    activity.mResumed = true;")
        sb.appendLine("    activity.onResume();")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
