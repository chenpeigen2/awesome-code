/**
 * ActivityThread 深入篇 - Instrumentation 与生命周期
 *
 * === Instrumentation 详解 ===
 *
 * Instrumentation 是 Android 中用于监控应用和系统交互的类。
 * 它负责调用 Activity 的所有生命周期方法。
 *
 * 【核心作用】
 * 1. 创建 Activity 实例
 * 2. 调用 Activity 的生命周期方法（onCreate, onStart, onResume 等）
 * 3. 调用 Application 的生命周期方法
 * 4. 支持测试框架（JUnit）
 *
 * 【与 ActivityThread 的关系】
 * ActivityThread 持有 Instrumentation 实例，
 * 所有生命周期回调都通过 Instrumentation 执行。
 */
