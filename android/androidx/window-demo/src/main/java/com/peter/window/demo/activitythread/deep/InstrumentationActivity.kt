    /**
     * Activity 创建过程
     */
    private fun showActivityCreation() {
        sb.appendLine("\n═══════════════════════════════════════════════════")
        sb.appendLine("           Activity 创建完整过程")
        sb.appendLine("═══════════════════════════════════════════════════\n")
        
        sb.appendLine("【performLaunchActivity 完整流程】\n")
        sb.appendLine("```java")
        sb.appendLine("private Activity performLaunchActivity(ActivityClientRecord r, Intent customIntent) {")
        sb.appendLine("    ")
        sb.appendLine("    // Step 1: 获取 ActivityInfo")
        sb.appendLine("    ActivityInfo aInfo = r.activityInfo;")
        sb.appendLine("    if (r.packageInfo == null) {")
        sb.appendLine("        r.packageInfo = getPackageInfo(aInfo.applicationInfo,")
        sb.appendLine("                r.compatInfo, Context.CONTEXT_INCLUDE_CODE);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // Step 2: 创建 ComponentName")
        sb.appendLine("    ComponentName component = r.intent.getComponent();")
        sb.appendLine("    if (component == null) {")
        sb.appendLine("        component = r.intent.resolveActivity(")
        sb.appendLine("                mInitialApplication.getPackageManager());")
        sb.appendLine("        r.intent.setComponent(component);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    if (r.activityInfo.targetActivity != null) {")
        sb.appendLine("        component = new ComponentName(r.activityInfo.packageName,")
        sb.appendLine("                r.activityInfo.targetActivity);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // Step 3: 创建 Activity Context")
        sb.appendLine("    ContextImpl appContext = createBaseContextForActivity(r);")
        sb.appendLine("    Activity activity = null;")
        sb.appendLine("    ")
        sb.appendLine("    // Step 4: 创建 Activity 实例")
        sb.appendLine("    try {")
        sb.appendLine("        java.lang.ClassLoader cl = appContext.getClassLoader();")
        sb.appendLine("        activity = mInstrumentation.newActivity(cl, component.getClassName(), r.intent);")
        sb.appendLine("        StrictMode.incrementExpectedActivityCount(activity.getClass());")
        sb.appendLine("        r.intent.setExtrasClassLoader(cl);")
        sb.appendLine("        r.intent.prepareToEnterProcess();")
        sb.appendLine("        if (r.state != null) {")
        sb.appendLine("            r.state.setClassLoader(cl);")
        sb.appendLine("        }")
        sb.appendLine("    } catch (Exception e) {")
        sb.appendLine("        if (!mInstrumentation.onException(activity, e)) {")
        sb.appendLine("            throw new RuntimeException(")
        sb.appendLine("                \"Unable to instantiate activity \" + component")
        sb.appendLine("                + \": \" + e.toString(), e);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // Step 5: 获取/创建 Application")
        sb.appendLine("    try {")
        sb.appendLine("        Application app = r.packageInfo.makeApplication(false, mInstrumentation);")
        sb.appendLine("        ")
        sb.appendLine("        if (activity != null) {")
        sb.appendLine("            // Step 6: 创建 Activity 的 Window")
        sb.appendLine("            CharSequence title = r.activityInfo.loadLabel(appContext.getPackageManager());")
        sb.appendLine("            Configuration config = new Configuration(mCompatConfiguration);")
        sb.appendLine("            ")
        sb.appendLine("            // Step 7: 调用 activity.attach()")
        sb.appendLine("            activity.attach(appContext, this, getInstrumentation(), r.token,")
        sb.appendLine("                    r.ident, app, r.intent, r.activityInfo, title, r.parent,")
        sb.appendLine("                    r.embeddedID, r.lastNonConfigurationInstances, config,")
        sb.appendLine("                    r.referrer, r.voiceInteractor, window, r.configCallback,")
        sb.appendLine("                    r.assistToken, r.shareableActivityToken);")
        sb.appendLine("            ")
        sb.appendLine("            if (customIntent != null) {")
        sb.appendLine("                activity.mIntent = customIntent;")
        sb.appendLine("            }")
        sb.appendLine("            r.lastNonConfigurationInstances = null;")
        sb.appendLine("            checkAndBlockForNetworkAccess();")
        sb.appendLine("            activity.mStartedActivity = false;")
        sb.appendLine("            int theme = r.activityInfo.getThemeResource();")
        sb.appendLine("            if (theme != 0) {")
        sb.appendLine("                activity.setTheme(theme);")
        sb.appendLine("            }")
        sb.appendLine("            ")
        sb.appendLine("            activity.mCalled = false;")
        sb.appendLine("            ")
        sb.appendLine("            // Step 8: 调用 onCreate")
        sb.appendLine("            if (r.isPersistable()) {")
        sb.appendLine("                mInstrumentation.callActivityOnCreate(activity, r.state, r.persistentState);")
        sb.appendLine("            } else {")
        sb.appendLine("                mInstrumentation.callActivityOnCreate(activity, r.state);")
        sb.appendLine("            }")
        sb.appendLine("            ")
        sb.appendLine("            if (!activity.mCalled) {")
        sb.appendLine("                throw new SuperNotCalledException(")
        sb.appendLine("                    \"Activity \" + r.intent.getComponent().toShortString() +")
        sb.appendLine("                    \" did not call through to super.onCreate()\");")
        sb.appendLine("            }")
        sb.appendLine("            r.activity = activity;")
        sb.appendLine("            mLastStartedActivityActivityRecord = r;")
        sb.appendLine("        }")
        sb.appendLine("        r.setState(ON_CREATE);")
        sb.appendLine("        ")
        sb.appendLine("        // Step 9: 保存 Activity 记录")
        sb.appendLine("        mActivities.put(r.token, r);")
        sb.appendLine("        ")
        sb.appendLine("    } catch (SuperNotCalledException e) {")
        sb.appendLine("        throw e;")
        sb.appendLine("    } catch (Exception e) {")
        sb.appendLine("        if (!mInstrumentation.onException(activity, e)) {")
        sb.appendLine("            throw new RuntimeException(")
        sb.appendLine("                \"Unable to start activity \" + component")
        sb.appendLine("                + \": \" + e.toString(), e);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    return activity;")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
