    /**
     * 同步屏障详解
     */
    private fun showSyncBarrier() {
        sb.clear()
        sb.appendLine("=== 同步屏障详解 ===\n")

        sb.appendLine("【什么是同步屏障】")
        sb.appendLine("同步屏障是一种特殊的 Message，它会阻止 MessageQueue ")
        sb.appendLine("处理同步消息，只允许异步消息通过。\n")

        sb.appendLine("【为什么需要同步屏障】")
        sb.appendLine("```\n")
        sb.appendLine("问题场景：")
        sb.appendLine("主线程 MessageQueue 中可能有大量同步消息")
        sb.appendLine("┌──────┐┌──────┐┌──────┐┌──────┐┌──────┐")
        sb.appendLine("│ Msg1 ││ Msg2 ││ Msg3 ││ Msg4 ││ Msg5 │ ...")
        sb.appendLine("└──────┘└──────┘└──────┘└──────┘└──────┘")
        sb.appendLine("如果 VSync 到来时，还在处理 Msg2...")
        sb.appendLine("→ UI 渲染被延迟")
        sb.appendLine("→ 可能导致掉帧\n")

        sb.appendLine("使用同步屏障后：")
        sb.appendLine("┌──────┐┌──────┐┌──────┐┌──────┐┌──────┐")
        sb.appendLine("│ Msg1 ││屏障  ││ Msg2 ││ Msg3 ││ Msg4 │")
        sb.appendLine("└──────┘└──────┘└──────┘└──────┘└──────┘")
        sb.appendLine("           ↓")
        sb.appendLine("      跳过同步消息")
        sb.appendLine("           ↓")
        sb.appendLine("    优先处理异步的 VSync")
        sb.appendLine("           ↓")
        sb.appendLine("    移除屏障后继续处理")
        sb.appendLine("```\n")

        sb.appendLine("【ViewRootImpl 使用同步屏障】")
        sb.appendLine("```java")
        sb.appendLine("// ViewRootImpl.scheduleTraversals()")
        sb.appendLine("void scheduleTraversals() {")
        sb.appendLine("    if (!mTraversalScheduled) {")
        sb.appendLine("        mTraversalScheduled = true;")
        sb.appendLine("        ")
        sb.appendLine("        // 1. 发送同步屏障")
        sb.appendLine("        mTraversalBarrier = mHandler.getLooper().getQueue()")
        sb.appendLine("                .postSyncBarrier();")
        sb.appendLine("        ")
        sb.appendLine("        // 2. 注册 Traversal 回调（异步消息）")
        sb.appendLine("        mChoreographer.postCallback(")
        sb.appendLine("                Choreographer.CALLBACK_TRAVERSAL,")
        sb.appendLine("                mTraversalRunnable, null);")
        sb.appendLine("    }")
        sb.appendLine("}\n")

        sb.appendLine("// ViewRootImpl.doTraversal()")
        sb.appendLine("void doTraversal() {")
        sb.appendLine("    if (mTraversalScheduled) {")
        sb.appendLine("        mTraversalScheduled = false;")
        sb.appendLine("        ")
        sb.appendLine("        // 3. 移除同步屏障")
        sb.appendLine("        mHandler.getLooper().getQueue()")
        sb.appendLine("                .removeSyncBarrier(mTraversalBarrier);")
        sb.appendLine("        ")
        sb.appendLine("        // 4. 执行遍历")
        sb.appendLine("        performTraversals();")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")

        sb.appendLine("【MessageQueue 中的同步屏障】")
        sb.appendLine("```java")
        sb.appendLine("// MessageQueue.next()")
        sb.appendLine("Message next() {")
        sb.appendLine("    for (;;) {")
        sb.appendLine("        synchronized (this) {")
        sb.appendLine("            // 获取下一条消息")
        sb.appendLine("            Message msg = mMessages;")
        sb.appendLine("            ")
        sb.appendLine("            // 检查是否有同步屏障")
        sb.appendLine("            if (msg != null && msg.target == null) {")
        sb.appendLine("                // 遇到同步屏障，只查找异步消息")
        sb.appendLine("                do {")
        sb.appendLine("                    Message prev = msg;")
        sb.appendLine("                    msg = prev.next;")
        sb.appendLine("                } while (msg != null && !msg.isAsynchronous());")
        sb.appendLine("            }")
        sb.appendLine("            ")
        sb.appendLine("            if (msg != null) {")
        sb.appendLine("                // 返回消息")
        sb.appendLine("                return msg;")
        sb.appendLine("            }")
        sb.appendLine("            // ...")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")

        sb.appendLine("【同步屏障原理】")
        sb.appendLine("```\n")
        sb.appendLine("MessageQueue 结构：")
        sb.appendLine("")
        sb.appendLine("没有屏障时：")
        sb.appendLine("mMessages → [Msg1] → [Msg2] → [Msg3] → null")
        sb.appendLine("             按时间顺序处理\n")

        sb.appendLine("插入屏障后：")
        sb.appendLine("mMessages → [Msg1] → [屏障] → [Msg2] → [Msg3(异步)] → [Msg4] → null")
        sb.appendLine("                      ↑")
        sb.appendLine("              target = null (屏障标志)")
        sb.appendLine("                      ")
        sb.appendLine("next() 时会跳过 Msg2，直接返回 Msg3（异步消息）")
        sb.appendLine("```\n")

        sb.appendLine("【异步消息】")
        sb.appendLine("```java")
        sb.appendLine("// 设置异步消息")
        sb.appendLine("Message msg = Message.obtain();")
        sb.appendLine("msg.what = MSG_TRAVERSAL;")
