    /**
     * HandlerActionQueue 详解
     */
    private fun showRunQueue() {
        sb.clear()
        sb.appendLine("=== RunQueue 详解 ===\n")

        sb.appendLine("【View.post 原理】")
        sb.appendLine("```java")
        sb.appendLine("// View.post()")
        sb.appendLine("public boolean post(Runnable action) {")
        sb.appendLine("    final AttachInfo attachInfo = mAttachInfo;")
        sb.appendLine("    if (attachInfo != null) {")
        sb.appendLine("        // View 已 attach，直接 post 到 Handler")
        sb.appendLine("        return attachInfo.mHandler.post(action);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // View 未 attach，加入 RunQueue")
        sb.appendLine("    getRunQueue().post(action);")
        sb.appendLine("    return true;")
        sb.appendLine("}")
        sb.appendLine("```\n")

        sb.appendLine("【HandlerActionQueue】")
        sb.appendLine("```java")
        sb.appendLine("// View 内部类，用于在 attach 前缓存 Runnable")
        sb.appendLine("private HandlerActionQueue getRunQueue() {")
        sb.appendLine("    if (mRunQueue == null) {")
        sb.appendLine("        mRunQueue = new HandlerActionQueue();")
        sb.appendLine("    }")
        sb.appendLine("    return mRunQueue;")
        sb.appendLine("}\n")

        sb.appendLine("// HandlerActionQueue")
        sb.appendLine("class HandlerActionQueue {")
        sb.appendLine("    private HandlerAction[] mActions;")
        sb.appendLine("    private int mCount;")
        sb.appendLine("    ")
        sb.appendLine("    public void post(Runnable action) {")
        sb.appendLine("        postDelayed(action, 0);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    public void postDelayed(Runnable action, long delayMillis) {")
        sb.appendLine("        // 将 Runnable 存入数组")
        sb.appendLine("        HandlerAction[] actions = mActions;")
        sb.appendLine("        // ... 扩容逻辑")
        sb.appendLine("        actions[mCount++] = new HandlerAction(action, delayMillis);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    public void executeActions(Handler handler) {")
        sb.appendLine("        // 将所有缓存的 Runnable 发送到 Handler")
        sb.appendLine("        for (int i = 0; i < mCount; i++) {")
        sb.appendLine("            HandlerAction action = mActions[i];")
        sb.appendLine("            handler.postDelayed(action.action, action.delay);")
        sb.appendLine("        }")
        sb.appendLine("        mCount = 0;")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")

        sb.appendLine("【执行时机】")
        sb.appendLine("```java")
        sb.appendLine("// ViewRootImpl.setView()")
        sb.appendLine("public void setView(View view, ...) {")
        sb.appendLine("    // ...")
        sb.appendLine("    view.assignParent(this);")
        sb.appendLine("    ")
        sb.appendLine("    // 执行缓存的 Runnable")
        sb.appendLine("    mRunQueue.executeActions(mAttachInfo.mHandler);")
        sb.appendLine("}")
        sb.appendLine("```\n")

        sb.appendLine("【RunQueue 流程图】")
        sb.appendLine("```\n")
        sb.appendLine("View 创建（未 attach）")
        sb.appendLine("        │")
        sb.appendLine("        ↓")
        sb.appendLine("view.post(runnable1)")
        sb.appendLine("        │")
        sb.appendLine("        ↓")
        sb.appendLine("    ┌─────────────────────┐")
        sb.appendLine("    │  HandlerActionQueue │")
        sb.appendLine("    │  [runnable1]        │")
        sb.appendLine("    │  [runnable2]        │")
        sb.appendLine("    │  [runnable3]        │")
        sb.appendLine("    └─────────────────────┘")
        sb.appendLine("        │")
        sb.appendLine("        ↓")
        sb.appendLine("ViewRootImpl.setView()")
        sb.appendLine("        │")
        sb.appendLine("        ↓")
        sb.appendLine("executeActions(handler)")
        sb.appendLine("        │")
        sb.appendLine("        ↓")
        sb.appendLine("  所有 Runnable 被发送到 Handler")
        sb.appendLine("        │")
        sb.appendLine("        ↓")
        sb.appendLine("  在下一帧执行")
        sb.appendLine("```\n")

        sb.appendLine("【注意事项】")
        sb.appendLine("```java")
        sb.appendLine("// 1. 在构造函数中使用 post")
        sb.appendLine("public MyView(Context context) {")
        sb.appendLine("    super(context);")
        sb.appendLine("    // 此时 View 未 attach，Runnable 被缓存")
        sb.appendLine("    post(() -> {")
        sb.appendLine("        // 在 attach 后执行")
        sb.appendLine("        // 此时可以获取 View 的宽高")
        sb.appendLine("        int width = getWidth();")
        sb.appendLine("    });")
        sb.appendLine("}\n")

        sb.appendLine("// 2. 不要在 post 中执行耗时操作")
        sb.appendLine("post(() -> {")
        sb.appendLine("    // 错误！会阻塞主线程")
        sb.appendLine("    Thread.sleep(1000);")
        sb.appendLine("});\n")

        sb.appendLine("// 3. 使用 postDelayed 延迟执行")
        sb.appendLine("postDelayed(() -> {")
        sb.appendLine("    // 延迟 100ms 后执行")
        sb.appendLine("}, 100);")
        sb.appendLine("```")

        binding.tvContent.text = sb.toString()
    }

