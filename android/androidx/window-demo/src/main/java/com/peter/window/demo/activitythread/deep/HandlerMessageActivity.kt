    /**
     * handleMessage 处理逻辑
     */
    private fun showHandleMessage() {
        sb.appendLine("\n═══════════════════════════════════════════════════")
        sb.appendLine("           handleMessage 处理逻辑")
        sb.appendLine("═══════════════════════════════════════════════════\n")
        
        sb.appendLine("【完整 handleMessage 实现】\n")
        sb.appendLine("```java")
        sb.appendLine("public void handleMessage(Message msg) {")
        sb.appendLine("    switch (msg.what) {")
        sb.appendLine("        ")
        sb.appendLine("        // ========== Activity 相关 ==========")
        sb.appendLine("        case LAUNCH_ACTIVITY: {")
        sb.appendLine("            ActivityClientRecord r = (ActivityClientRecord) msg.obj;")
        sb.appendLine("            r.packageInfo = getPackageInfoNoCheck(")
        sb.appendLine("                    r.activityInfo.applicationInfo, r.compatInfo);")
        sb.appendLine("            handleLaunchActivity(r, null, \"LAUNCH_ACTIVITY\");")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case PAUSE_ACTIVITY: {")
        sb.appendLine("            SomeArgs args = (SomeArgs) msg.obj;")
        sb.appendLine("            handlePauseActivity((IBinder) args.arg1,")
        sb.appendLine("                    args.argi1 != 0, msg.arg1 != 0, args.argi2);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case RESUME_ACTIVITY: {")
        sb.appendLine("            handleResumeActivity((IBinder) msg.obj, true,")
        sb.appendLine("                    msg.arg1 != 0, true, msg.arg2, \"RESUME_ACTIVITY\");")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case STOP_ACTIVITY_SHOW: {")
        sb.appendLine("            handleStopActivity((IBinder) msg.obj, true, msg.arg2);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case STOP_ACTIVITY_HIDE: {")
        sb.appendLine("            handleStopActivity((IBinder) msg.obj, false, msg.arg2);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case DESTROY_ACTIVITY: {")
        sb.appendLine("            handleDestroyActivity((IBinder) msg.obj, msg.arg1 != 0,")
        sb.appendLine("                    msg.arg2, false);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        // ========== Service 相关 ==========")
        sb.appendLine("        case CREATE_SERVICE: {")
        sb.appendLine("            handleCreateService((CreateServiceData) msg.obj);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case BIND_SERVICE: {")
        sb.appendLine("            handleBindService((BindServiceData) msg.obj);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case UNBIND_SERVICE: {")
        sb.appendLine("            handleUnbindService((BindServiceData) msg.obj);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case STOP_SERVICE: {")
        sb.appendLine("            handleStopService((IBinder) msg.obj);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        // ========== Application 相关 ==========")
        sb.appendLine("        case BIND_APPLICATION: {")
        sb.appendLine("            AppBindData data = (AppBindData) msg.obj;")
        sb.appendLine("            handleBindApplication(data);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case EXIT_APPLICATION: {")
        sb.appendLine("            if (mInitialApplication != null) {")
        sb.appendLine("                mInitialApplication.onTerminate();")
        sb.appendLine("            }")
        sb.appendLine("            Looper.myLooper().quit();")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        // ========== 系统事件 ==========")
        sb.appendLine("        case CONFIGURATION_CHANGED: {")
        sb.appendLine("            handleConfigurationChanged((Configuration) msg.obj);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case LOW_MEMORY: {")
        sb.appendLine("            handleLowMemory();")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        case TRIM_MEMORY: {")
        sb.appendLine("            handleTrimMemory(msg.arg1);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        // ========== Provider 相关 ==========")
        sb.appendLine("        case INSTALL_PROVIDER: {")
        sb.appendLine("            handleInstallProvider((ProviderInfo) msg.obj);")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        // ========== 新版事务处理 ==========")
        sb.appendLine("        case EXECUTE_TRANSACTION: {")
        sb.appendLine("            final ClientTransaction transaction = (ClientTransaction) msg.obj;")
        sb.appendLine("            mTransactionExecutor.execute(transaction);")
        sb.appendLine("            if (isSystem()) {")
        sb.appendLine("                transaction.recycle();")
        sb.appendLine("            }")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【消息处理流程图】\n")
        sb.appendLine("```\n")
        sb.appendLine("ApplicationThread              H (Handler)              处理方法")
        sb.appendLine("       │                          │                        │")
        sb.appendLine("       │ scheduleLaunchActivity   │                        │")
        sb.appendLine("       │ sendMessage(LAUNCH)      │                        │")
        sb.appendLine("       │─────────────────────────>│                        │")
        sb.appendLine("       │                          │ handleMessage()        │")
        sb.appendLine("       │                          │ case LAUNCH_ACTIVITY:  │")
        sb.appendLine("       │                          │ handleLaunchActivity() │")
        sb.appendLine("       │                          │───────────────────────>│")
        sb.appendLine("       │                          │                        │")
        sb.appendLine("       │ schedulePauseActivity    │                        │")
        sb.appendLine("       │ sendMessage(PAUSE)       │                        │")
        sb.appendLine("       │─────────────────────────>│                        │")
