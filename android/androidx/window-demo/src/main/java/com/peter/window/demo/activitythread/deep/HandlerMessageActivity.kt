        sb.appendLine("【如何 Hook H 类】\n")
        sb.appendLine("```java")
        sb.appendLine("public class HHook {")
        sb.appendLine("    ")
        sb.appendLine("    private static final String TAG = \"HHook\";")
        sb.appendLine("    ")
        sb.appendLine("    public static void hook() {")
        sb.appendLine("        try {")
        sb.appendLine("            // 1. 获取 ActivityThread 实例")
        sb.appendLine("            Class<?> activityThreadClass = Class.forName(\"android.app.ActivityThread\");")
        sb.appendLine("            Method currentActivityThreadMethod = ")
        sb.appendLine("                    activityThreadClass.getDeclaredMethod(\"currentActivityThread\");")
        sb.appendLine("            Object activityThread = currentActivityThreadMethod.invoke(null);")
        sb.appendLine("            ")
        sb.appendLine("            // 2. 获取原始的 H Handler")
        sb.appendLine("            Field mHField = activityThreadClass.getDeclaredField(\"mH\");")
        sb.appendLine("            mHField.setAccessible(true);")
        sb.appendLine("            Handler originalHandler = (Handler) mHField.get(activityThread);")
        sb.appendLine("            ")
        sb.appendLine("            // 3. 获取 Handler 的 mCallback 字段")
        sb.appendLine("            Field mCallbackField = Handler.class.getDeclaredField(\"mCallback\");")
        sb.appendLine("            mCallbackField.setAccessible(true);")
        sb.appendLine("            ")
        sb.appendLine("            // 4. 创建自定义 Callback")
        sb.appendLine("            Handler.Callback customCallback = new Handler.Callback() {")
        sb.appendLine("                @Override")
        sb.appendLine("                public boolean handleMessage(Message msg) {")
        sb.appendLine("                    // 拦截消息")
        sb.appendLine("                    Log.d(TAG, \"Intercepted message: \" + msg.what);")
