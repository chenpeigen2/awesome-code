        sb.appendLine("            }")
        sb.appendLine("        }")
        sb.appendLine("        // 3. 最后执行 Handler.handleMessage()")
        sb.appendLine("        handleMessage(msg);")
        sb.appendLine("    }")
        sb.appendLine("}")
