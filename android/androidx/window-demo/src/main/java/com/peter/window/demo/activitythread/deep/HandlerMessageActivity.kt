        sb.appendLine("                            break;")
        sb.appendLine("                    }")
        sb.appendLine("                    ")
        sb.appendLine("                    // 返回 false 让原始 Handler 继续处理")
        sb.appendLine("                    return false;")
        sb.appendLine("                }")
        sb.appendLine("            };")
        sb.appendLine("            ")
        sb.appendLine("            // 5. 替换 Callback")
        sb.appendLine("            mCallbackField.set(originalHandler, customCallback);")
        sb.appendLine("            ")
        sb.appendLine("            Log.d(TAG, \"H Hook success!\");")
        sb.appendLine("            ")
        sb.appendLine("        } catch (Exception e) {")
