        sb.appendLine("═══════════════════════════════════════════════════\n")
        
        sb.appendLine("【Hook H Handler】\n")
        sb.appendLine("```java")
        sb.appendLine("public class HandlerHook {")
        sb.appendLine("    ")
        sb.appendLine("    private static final String TAG = \"HandlerHook\";")
        sb.appendLine("    ")
        sb.appendLine("    public static void hook() {")
        sb.appendLine("        try {")
        sb.appendLine("            // 1. 获取 ActivityThread")
        sb.appendLine("            Class<?> activityThreadClass = Class.forName(\"android.app.ActivityThread\");")
        sb.appendLine("            Field currentActivityThreadField = ")
        sb.appendLine("                    activityThreadClass.getDeclaredField(\"sCurrentActivityThread\");")
        sb.appendLine("            currentActivityThreadField.setAccessible(true);")
        sb.appendLine("            Object activityThread = currentActivityThreadField.get(null);")
        sb.appendLine("            ")
        sb.appendLine("            // 2. 获取 mH")
        sb.appendLine("            Field mHField = activityThreadClass.getDeclaredField(\"mH\");")
        sb.appendLine("            mHField.setAccessible(true);")
        sb.appendLine("            Handler mH = (Handler) mHField.get(activityThread);")
        sb.appendLine("            ")
        sb.appendLine("            // 3. 获取 mCallback 字段")
        sb.appendLine("            Field mCallbackField = Handler.class.getDeclaredField(\"mCallback\");")
        sb.appendLine("            mCallbackField.setAccessible(true);")
        sb.appendLine("            ")
        sb.appendLine("            // 4. 创建代理 Callback")
        sb.appendLine("            Handler.Callback proxyCallback = new Handler.Callback() {")
        sb.appendLine("                @Override")
        sb.appendLine("                public boolean handleMessage(Message msg) {")
        sb.appendLine("                    String messageName = getMessageName(msg.what);")
        sb.appendLine("                    Log.d(TAG, \"拦截消息: \" + messageName + \" (\" + msg.what + \")\");")
        sb.appendLine("                    ")
        sb.appendLine("                    long start = System.currentTimeMillis();")
        sb.appendLine("                    ")
        sb.appendLine("                    // 可以在这里做监控或拦截")
        sb.appendLine("                    switch (msg.what) {")
        sb.appendLine("                        case 100: // LAUNCH_ACTIVITY")
