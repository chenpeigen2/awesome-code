        sb.appendLine("            mInstrumentationField.set(activityThread, monitor);")
        sb.appendLine("            ")
        sb.appendLine("        } catch (Exception e) {")
        sb.appendLine("            Log.e(\"LifecycleMonitor\", \"安装失败\", e);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
    }

