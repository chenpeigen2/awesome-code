        sb.appendLine("        dispatchLifecycleEnd(activity, \"onCreate\", cost);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    @Override")
        sb.appendLine("    public void callActivityOnStart(Activity activity) {")
