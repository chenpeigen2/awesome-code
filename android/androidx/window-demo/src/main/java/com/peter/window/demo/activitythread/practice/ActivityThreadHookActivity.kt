        sb.appendLine("【完整的卡顿检测实现】\n")
        sb.appendLine("```java")
        sb.appendLine("public class BlockDetector {")
        sb.appendLine("    ")
        sb.appendLine("    private static final String TAG = \"BlockDetector\";")
        sb.appendLine("    private static final long BLOCK_THRESHOLD_MS = 100;")
        sb.appendLine("    private static final long SAMPLE_INTERVAL_MS = 50;")
        sb.appendLine("    ")
        sb.appendLine("    private static volatile boolean sSampling = false;")
        sb.appendLine("    private static List<String> sStackSamples = new ArrayList<>();")
        sb.appendLine("    private static long sStartTime;")
        sb.appendLine("    ")
        sb.appendLine("    public static void install() {")
        sb.appendLine("        Looper.getMainLooper().setMessageLogging(new Printer() {")
        sb.appendLine("            @Override")
        sb.appendLine("            public void println(String x) {")
        sb.appendLine("                if (x.startsWith(\">>>>> Dispatching\")) {")
        sb.appendLine("                    startSampling();")
        sb.appendLine("                } else if (x.startsWith(\"<<<<< Finished\")) {")
        sb.appendLine("                    stopSampling();")
        sb.appendLine("                }")
        sb.appendLine("            }")
        sb.appendLine("        });")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    private static void startSampling() {")
        sb.appendLine("        sStartTime = System.currentTimeMillis();")
        sb.appendLine("        sSampling = true;")
        sb.appendLine("        sStackSamples.clear();")
        sb.appendLine("        ")
        sb.appendLine("        new Thread(() -> {")
        sb.appendLine("            while (sSampling) {")
        sb.appendLine("                try {")
        sb.appendLine("                    Thread.sleep(SAMPLE_INTERVAL_MS);")
        sb.appendLine("                    ")
        sb.appendLine("                    // 采集主线程堆栈")
        sb.appendLine("                    StringBuilder sb = new StringBuilder();")
        sb.appendLine("                    sb.append(\"\\n=== \").append(System.currentTimeMillis() - sStartTime)")
        sb.appendLine("                            .append(\"ms ===\");")
        sb.appendLine("                    ")
        sb.appendLine("                    StackTraceElement[] stack = Looper.getMainLooper()")
        sb.appendLine("                            .getThread().getStackTrace();")
        sb.appendLine("                    for (StackTraceElement e : stack) {")
        sb.appendLine("                        sb.append(\"\\n    at \").append(e.toString());")
        sb.appendLine("                    }")
        sb.appendLine("                    ")
        sb.appendLine("                    synchronized (sStackSamples) {")
        sb.appendLine("                        sStackSamples.add(sb.toString());")
        sb.appendLine("                    }")
        sb.appendLine("                    ")
        sb.appendLine("                } catch (InterruptedException e) {")
        sb.appendLine("                    break;")
        sb.appendLine("                }")
        sb.appendLine("            }")
        sb.appendLine("        }).start();")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    private static void stopSampling() {")
        sb.appendLine("        sSampling = false;")
        sb.appendLine("        long cost = System.currentTimeMillis() - sStartTime;")
        sb.appendLine("        ")
        sb.appendLine("        if (cost > BLOCK_THRESHOLD_MS) {")
        sb.appendLine("            StringBuilder report = new StringBuilder();")
        sb.appendLine("            report.append(\"\\n====== 卡顿报告 ======\\n\");")
        sb.appendLine("            report.append(\"耗时: \").append(cost).append(\"ms\\n\");")
        sb.appendLine("            ")
        sb.appendLine("            synchronized (sStackSamples) {")
        sb.appendLine("                for (String sample : sStackSamples) {")
        sb.appendLine("                    report.append(sample);")
        sb.appendLine("                }")
        sb.appendLine("            }")
        sb.appendLine("            ")
        sb.appendLine("            report.append(\"\\n=====================\");")
        sb.appendLine("            ")
        sb.appendLine("            Log.w(TAG, report.toString());")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
    }
