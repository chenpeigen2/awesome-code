        sb.appendLine("```java")
        sb.appendLine("private void attach(boolean system, long startSeq) {")
        sb.appendLine("    sCurrentActivityThread = this;")
        sb.appendLine("    mSystemThread = system;")
        sb.appendLine("    ")
        sb.appendLine("    if (!system) {")
        sb.appendLine("        // 获取 AMS 代理")
