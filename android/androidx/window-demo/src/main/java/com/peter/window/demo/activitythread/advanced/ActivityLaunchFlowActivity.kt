        sb.appendLine("```java")
        sb.appendLine("// 在 H (Handler) 中处理 LAUNCH_ACTIVITY 消息")
        sb.appendLine("public void handleMessage(Message msg) {")
        sb.appendLine("    switch (msg.what) {")
        sb.appendLine("        case LAUNCH_ACTIVITY: {")
        sb.appendLine("            ActivityClientRecord r = (ActivityClientRecord) msg.obj;")
        sb.appendLine("            r.packageInfo = getPackageInfoNoCheck(...);")
        sb.appendLine("            handleLaunchActivity(r, null, \"LAUNCH_ACTIVITY\");")
        sb.appendLine("            break;")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("")
        sb.appendLine("private void handleLaunchActivity(ActivityClientRecord r,") 
        sb.appendLine("        Intent customIntent, String reason) {")
        sb.appendLine("    ")
        sb.appendLine("    // 1. 初始化 WindowManagerService 相关")
        sb.appendLine("    WindowManagerGlobal.initialize();")
        sb.appendLine("    ")
        sb.appendLine("    // 2. 创建 Activity")
        sb.appendLine("    Activity a = performLaunchActivity(r, customIntent);")
        sb.appendLine("    ")
        sb.appendLine("    if (a != null) {")
        sb.appendLine("        r.createdConfig = new Configuration(mConfiguration);")
        sb.appendLine("        reportSizeConfigurations(r);")
        sb.appendLine("        ")
        sb.appendLine("        // 3. 调用 Activity.onResume()")
        sb.appendLine("        handleResumeActivity(r.token, false, r.isForward,")
        sb.appendLine("                !r.activity.mFinished && !r.startsNotResumed,")
        sb.appendLine("                r.lastProcessedSeq, reason);")
        sb.appendLine("        ")
        sb.appendLine("        if (!r.activity.mFinished && r.startsNotResumed) {")
        sb.appendLine("            // 4. 如果需要，调用 Activity.onPause()")
        sb.appendLine("            performPauseActivityIfNeeded(r, reason);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【performLaunchActivity - 核心创建流程】\n")
        sb.appendLine("```java")
        sb.appendLine("private Activity performLaunchActivity(ActivityClientRecord r, Intent customIntent) {")
        sb.appendLine("    ")
        sb.appendLine("    // 1. 获取 ActivityInfo")
        sb.appendLine("    ActivityInfo aInfo = r.activityInfo;")
        sb.appendLine("    ComponentName component = new ComponentName(aInfo.applicationInfo.packageName,")
        sb.appendLine("            aInfo.name);")
        sb.appendLine("    ")
        sb.appendLine("    // 2. 创建/获取 Application")
        sb.appendLine("    Application app = r.packageInfo.makeApplication(false, mInstrumentation);")
        sb.appendLine("    ")
        sb.appendLine("    // 3. 创建 Activity 实例")
        sb.appendLine("    Activity activity = null;")
        sb.appendLine("    try {")
        sb.appendLine("        java.lang.ClassLoader cl = appContext.getClassLoader();")
        sb.appendLine("        activity = mInstrumentation.newActivity(cl, component.getClassName(), r.intent);")
        sb.appendLine("    } catch (Exception e) {")
        sb.appendLine("        // 处理异常")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    if (activity != null) {")
        sb.appendLine("        // 4. 创建 Activity 的 Context")
        sb.appendLine("        Context appContext = createBaseContextForActivity(r, activity);")
        sb.appendLine("        ")
        sb.appendLine("        // 5. 调用 Activity.attach()")
