/**
 * ActivityThread 进阶篇 - Activity 启动流程
 *
 * === 完整启动流程 ===
 *
 * Activity 启动涉及多个进程：
 * 1. Launcher 进程（桌面）
 * 2. System Server 进程（AMS 所在进程）
 * 3. Zygote 进程（孵化进程）
 * 4. App 进程（目标应用进程）
 *
 * 【整体架构】
 * ```
 * Launcher 进程          System Server 进程           App 进程
 *     │                       │                          │
 *     │ startActivity()       │                          │
 *     │──────────────────────>│                          │
 *     │                       │                          │
 *     │                       │ startProcess()           │
 *     │                       │──────────────────────>Zygote
 *     │                       │                          │
 *     │                       │      fork app process    │
 *     │                       │<─────────────────────────│
 *     │                       │                          │
 *     │                       │ attachApplication()      │
 *     │                       │─────────────────────────>│
 *     │                       │                          │
 *     │                       │      scheduleLaunchActivity
 *     │                       │─────────────────────────>│
 *     │                       │                          │
 *     │                       │        handleLaunchActivity
 *     │                       │                          │
 *     │                       │        performLaunchActivity
 *     │                       │                          │
 *     │                       │        Activity.onCreate()
 *     │                       │                          │
 * ```
 */
class ActivityLaunchFlowActivity : AppCompatActivity() {

