        sb.appendLine("        ")
        sb.appendLine("        // 如果需要持续监听，需要重新注册")
        sb.appendLine("        Choreographer.getInstance().postFrameCallback(this);")
        sb.appendLine("    }")
        sb.appendLine("});")
        sb.appendLine("```\n")
        
        sb.appendLine("【延迟注册】")
        sb.appendLine("```java")
        sb.appendLine("// 延迟一帧后注册")
        sb.appendLine("public void postFrameCallbackDelayed(FrameCallback callback, long delayMillis) {")
        sb.appendLine("    if (callback == null) {")
        sb.appendLine("        throw new IllegalArgumentException(\"callback must not be null\");")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 转换为纳秒")
        sb.appendLine("    final long dueTime = SystemClock.uptimeMillis() + delayMillis;")
        sb.appendLine("    postCallbackDelayedInternal(CALLBACK_ANIMATION, callback, null, dueTime);")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【计算帧时间】")
        sb.appendLine("```java")
        sb.appendLine("// 计算与上一帧的时间差")
        sb.appendLine("long frameIntervalNanos = frameTimeNanos - mLastFrameTimeNanos;")
        sb.appendLine("")
        sb.appendLine("// 标准帧间隔（60fps = 16.67ms）")
        sb.appendLine("long standardFrameInterval = (long)(1000000000 / 60);  // 约 16.67ms")
        sb.appendLine("")
        sb.appendLine("// 判断是否掉帧")
        sb.appendLine("if (frameIntervalNanos > standardFrameInterval * 2) {")
        sb.appendLine("    // 掉帧了")
        sb.appendLine("    int skippedFrames = (int)(frameIntervalNanos / standardFrameInterval);")
        sb.appendLine("    Log.d(\"Jank\", \"Skipped \" + skippedFrames + \" frames!\");")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【实用示例：帧率监控】")
        sb.appendLine("```java")
