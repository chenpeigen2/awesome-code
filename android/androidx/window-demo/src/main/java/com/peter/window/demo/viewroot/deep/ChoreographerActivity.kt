        sb.appendLine("     ├── [3] CALLBACK_TRAVERSAL")
        sb.appendLine("     │        │")
        sb.appendLine("     │        ↓")
        sb.appendLine("     │    CallbackRecord (performTraversals)")
        sb.appendLine("     │")
        sb.appendLine("     └── [4] CALLBACK_COMMIT")
        sb.appendLine("```\n")
        
        sb.appendLine("【doCallbacks】")
        sb.appendLine("```java")
        sb.appendLine("void doCallbacks(int callbackType, long frameTimeNanos) {")
        sb.appendLine("    CallbackRecord callbacks;")
        sb.appendLine("    ")
        sb.appendLine("    synchronized (mLock) {")
        sb.appendLine("        // 获取到期的回调")
        sb.appendLine("        final long now = System.nanoTime();")
        sb.appendLine("        callbacks = mCallbackQueues[callbackType]")
        sb.appendLine("            .extractDueCallbacksLocked(now);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 执行回调")
        sb.appendLine("    try {")
        sb.appendLine("        for (CallbackRecord c = callbacks; c != null; c = c.next) {")
        sb.appendLine("            c.run(frameTimeNanos);")
        sb.appendLine("        }")
        sb.appendLine("    } finally {")
        sb.appendLine("        // 回收回调")
        sb.appendLine("        synchronized (mLock) {")
        sb.appendLine("            do {")
        sb.appendLine("                final CallbackRecord next = callbacks.next;")
        sb.appendLine("                recycleCallbackLocked(callbacks);")
        sb.appendLine("                callbacks = next;")
        sb.appendLine("            } while (callbacks != null);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        binding.tvContent.text = sb.toString()
    }

