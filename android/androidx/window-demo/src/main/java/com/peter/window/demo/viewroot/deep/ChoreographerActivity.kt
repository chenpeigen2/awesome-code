    /**
     * VSync 机制详解
     */
    private fun showVSyncMechanism() {
        sb.clear()
        sb.appendLine("=== VSync 机制详解 ===\n")
        
        sb.appendLine("【什么是 VSync】")
        sb.appendLine("VSync（Vertical Synchronization，垂直同步）是屏幕刷新信号。")
        sb.appendLine("每当屏幕刷新完一帧后，会发出一个 VSync 信号。\n")
        
        sb.appendLine("【屏幕刷新率】")
        sb.appendLine("```\n")
        sb.appendLine("常见刷新率：")
        sb.appendLine("- 60Hz: 每 16.67ms 刷新一次")
        sb.appendLine("- 90Hz: 每 11.11ms 刷新一次")
        sb.appendLine("- 120Hz: 每 8.33ms 刷新一次")
        sb.appendLine("```\n")
        
        sb.appendLine("【VSync 的作用】")
        sb.appendLine("```\n")
        sb.appendLine("没有 VSync：")
        sb.appendLine("┌──────────────────────────────────────────────────┐\n")
        sb.appendLine("│ GPU 随时绘制 → 可能与屏幕刷新不同步 → 画面撕裂  │\n")
        sb.appendLine("└──────────────────────────────────────────────────┘\n")
        sb.appendLine("")
        sb.appendLine("有 VSync：")
        sb.appendLine("┌──────────────────────────────────────────────────┐\n")
        sb.appendLine("│ VSync 信号 → 开始绘制 → 下一帧 VSync → 显示     │\n")
        sb.appendLine("│ 确保每帧都能在屏幕刷新时完整显示                 │\n")
        sb.appendLine("└──────────────────────────────────────────────────┘\n")
        sb.appendLine("```\n")
        
        sb.appendLine("【FrameDisplayEventReceiver】")
        sb.appendLine("```java")
        sb.appendLine("// Choreographer 内部类，接收 VSync 信号")
        sb.appendLine("private final class FrameDisplayEventReceiver extends DisplayEventReceiver")
        sb.appendLine("        implements Runnable {")
        sb.appendLine("    ")
        sb.appendLine("    @Override")
        sb.appendLine("    public void onVsync(long timestampNanos, int builtInDisplayId, int frame) {")
        sb.appendLine("        // 保存 VSync 时间戳")
        sb.appendLine("        mTimestampNanos = timestampNanos;")
        sb.appendLine("        mFrame = frame;")
        sb.appendLine("        ")
        sb.appendLine("        // 将自己作为 Runnable 发送到主线程")
        sb.appendLine("        Message msg = Message.obtain(mHandler, this);")
        sb.appendLine("        msg.setAsynchronous(true);  // 异步消息")
        sb.appendLine("        mHandler.sendMessageAtFrontOfQueue(msg);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    @Override")
        sb.appendLine("    public void run() {")
        sb.appendLine("        // 在主线程执行帧处理")
        sb.appendLine("        doFrame(mTimestampNanos, mFrame);")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【scheduleVsync】")
        sb.appendLine("```java")
        sb.appendLine("// 请求 VSync 信号")
        sb.appendLine("private void scheduleVsyncLocked() {")
        sb.appendLine("    mDisplayEventReceiver.scheduleVsync();")
        sb.appendLine("}")
        sb.appendLine("")
        sb.appendLine("// DisplayEventReceiver.native 方法")
        sb.appendLine("public void scheduleVsync() {")
        sb.appendLine("    if (mReceiverPtr == 0) {")
        sb.appendLine("        return;")
        sb.appendLine("    }")
        sb.appendLine("    nativeScheduleVsync(mReceiverPtr);")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【VSync 流程图】")
        sb.appendLine("```\n")
        sb.appendLine("scheduleTraversals()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("Choreographer.postCallback(CALLBACK_TRAVERSAL)")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("scheduleVsyncLocked()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("nativeScheduleVsync() → 请求 VSync")
        sb.appendLine("       │")
        sb.appendLine("       ↓ (等待 VSync)")
        sb.appendLine("onVsync(timestampNanos)")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("Message.setAsynchronous(true)")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("doFrame(timestampNanos)")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("doCallbacks(CALLBACK_TRAVERSAL)")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("performTraversals()")
        sb.appendLine("```\n")
        
        sb.appendLine("【双重缓冲 vs 三重缓冲】")
        sb.appendLine("```\n")
        sb.appendLine("双重缓冲：")
        sb.appendLine("  - 前缓冲：正在显示")
        sb.appendLine("  - 后缓冲：正在绘制")
        sb.appendLine("  - GPU 绘制完成后等待 VSync 交换")
        sb.appendLine("")
        sb.appendLine("三重缓冲：")
        sb.appendLine("  - 前缓冲：正在显示")
        sb.appendLine("  - 后缓冲：等待显示")
        sb.appendLine("  - 第三缓冲：正在绘制")
        sb.appendLine("  - 可以让 GPU 不用等待，继续绘制下一帧")
        sb.appendLine("  - 代价是多一帧延迟")
        sb.appendLine("```\n")
        
        binding.tvContent.text = sb.toString()
    }

