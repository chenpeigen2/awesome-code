        sb.appendLine("            ((FrameCallback) action).doFrame(frameTimeNanos);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【回调队列数组】")
        sb.appendLine("```java")
        sb.appendLine("// Choreographer 持有多个队列")
        sb.appendLine("private final CallbackQueue[] mCallbackQueues;")
        sb.appendLine("")
        sb.appendLine("// 初始化")
        sb.appendLine("mCallbackQueues = new CallbackQueue[CALLBACK_LAST + 1];")
        sb.appendLine("for (int i = 0; i <= CALLBACK_LAST; i++) {")
        sb.appendLine("    mCallbackQueues[i] = new CallbackQueue();")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【队列结构图】")
        sb.appendLine("```\n")
        sb.appendLine("mCallbackQueues[]")
        sb.appendLine("     │")
        sb.appendLine("     ├── [0] CALLBACK_INPUT")
        sb.appendLine("     │        │")
        sb.appendLine("     │        ↓")
