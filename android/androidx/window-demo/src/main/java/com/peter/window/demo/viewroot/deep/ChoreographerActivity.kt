        sb.appendLine("    private long mLastFrameTimeNanos = 0;")
        sb.appendLine("    private int mFrameCount = 0;")
        sb.appendLine("    private long mStartTimeNanos = 0;")
        sb.appendLine("    ")
        sb.appendLine("    public void start() {")
        sb.appendLine("        mStartTimeNanos = System.nanoTime();")
        sb.appendLine("        Choreographer.getInstance().postFrameCallback(this);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    public void stop() {")
        sb.appendLine("        Choreographer.getInstance().removeFrameCallback(this);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    @Override")
        sb.appendLine("    public void doFrame(long frameTimeNanos) {")
        sb.appendLine("        mFrameCount++;")
        sb.appendLine("        ")
        sb.appendLine("        // 每秒计算一次 FPS")
        sb.appendLine("        long elapsedNanos = frameTimeNanos - mStartTimeNanos;")
        sb.appendLine("        if (elapsedNanos >= 1000000000L) {  // 1 秒")
        sb.appendLine("            float fps = mFrameCount * 1000000000f / elapsedNanos;")
        sb.appendLine("            Log.d(\"FPS\", String.format(\"FPS: %.1f\", fps));")
        sb.appendLine("            mFrameCount = 0;")
        sb.appendLine("            mStartTimeNanos = frameTimeNanos;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        // 掉帧检测")
        sb.appendLine("        if (mLastFrameTimeNanos != 0) {")
        sb.appendLine("            long frameInterval = frameTimeNanos - mLastFrameTimeNanos;")
        sb.appendLine("            if (frameInterval > 16666666L * 2) {  // 超过 2 帧")
        sb.appendLine("                int skipped = (int)(frameInterval / 16666666L);")
        sb.appendLine("                Log.w(\"Jank\", \"Skipped \" + skipped + \" frames\");")
        sb.appendLine("            }")
        sb.appendLine("        }")
        sb.appendLine("        mLastFrameTimeNanos = frameTimeNanos;")
        sb.appendLine("        ")
        sb.appendLine("        // 继续监听下一帧")
        sb.appendLine("        Choreographer.getInstance().postFrameCallback(this);")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【注意事项】")
        sb.appendLine("1. doFrame 在主线程执行")
        sb.appendLine("2. 不要在 doFrame 中执行耗时操作")
        sb.appendLine("3. 需要持续监听要重新注册")
        sb.appendLine("4. 使用 removeFrameCallback 取消注册")
        
        binding.tvContent.text = sb.toString()
    }

