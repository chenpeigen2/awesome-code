    /**
     * Choreographer 实践演示
     */
    private fun demonstrateChoreographer() {
        sb.clear()
        sb.appendLine("=== Choreographer 实践演示 ===\n")
        
        sb.appendLine("【演示：使用 FrameCallback 监控帧率】\n")
        
        // 创建帧率监控器
        val frameCallback = object : Choreographer.FrameCallback {
            private var lastFrameTimeNanos = 0L
            private var frameCount = 0
            private var startTimeNanos = 0L
            
            override fun doFrame(frameTimeNanos: Long) {
                if (startTimeNanos == 0L) {
                    startTimeNanos = frameTimeNanos
                }
                
                frameCount++
                
                // 计算帧间隔
                if (lastFrameTimeNanos != 0L) {
                    val frameInterval = (frameTimeNanos - lastFrameTimeNanos) / 1_000_000
                    sb.appendLine("帧间隔: ${frameInterval}ms")
                    
                    // 检测掉帧
                    if (frameInterval > 33) {  // 超过 2 帧
                        sb.appendLine("  ⚠️ 可能掉帧!")
                    }
                }
                
                // 每秒计算一次 FPS
                val elapsed = frameTimeNanos - startTimeNanos
                if (elapsed >= 1_000_000_000L) {
                    val fps = frameCount * 1_000_000_000f / elapsed
                    sb.appendLine("\n=== FPS: ${"%.1f".format(fps)} ===\n")
                    frameCount = 0
                    startTimeNanos = frameTimeNanos
                }
                
                lastFrameTimeNanos = frameTimeNanos
                
                // 继续监听
                if (frameCount < 100) {  // 限制监听帧数
                    Choreographer.getInstance().postFrameCallback(this)
                }
                
                binding.tvContent.text = sb.toString()
            }
        }
        
        sb.appendLine("开始监听帧率...\n")
        
        // 开始监听
        Choreographer.getInstance().postFrameCallback(frameCallback)
        
        binding.tvContent.text = sb.toString()
    }

