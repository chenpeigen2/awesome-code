        sb.appendLine("```java")
        sb.appendLine("// 回调记录")
        sb.appendLine("private static final class CallbackRecord {")
        sb.appendLine("    public CallbackRecord next;      // 下一个")
        sb.appendLine("    public long dueTime;             // 到期时间")
        sb.appendLine("    public Object action;            // Runnable 或 FrameCallback")
