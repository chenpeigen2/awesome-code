    private fun showInputStage() {
        sb.clear()
        sb.appendLine("=== InputStage 责任链 ===\n")
        
        sb.appendLine("【InputStage 结构】")
        sb.appendLine("```java")
        sb.appendLine("abstract class InputStage {")
        sb.appendLine("    private final InputStage mNext;  // 下一个 Stage")
        sb.appendLine("    ")
        sb.appendLine("    public InputStage(InputStage next) {")
        sb.appendLine("        mNext = next;")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    public final void deliver(QueuedInputEvent q) {")
        sb.appendLine("        if ((q.mFlags & QueuedInputEvent.FLAG_FINISHED) != 0) {")
        sb.appendLine("            forward(q);")
        sb.appendLine("        } else if (shouldDropInputEvent(q)) {")
        sb.appendLine("            finish(q, false);")
        sb.appendLine("        } else {")
        sb.appendLine("            apply(q, onProcess(q));")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    protected abstract int onProcess(QueuedInputEvent q);")
        sb.appendLine("    ")
        sb.appendLine("    protected void finish(QueuedInputEvent q, boolean handled) {")
        sb.appendLine("        q.mFlags |= QueuedInputEvent.FLAG_FINISHED;")
        sb.appendLine("        if (mNext != null) {")
        sb.appendLine("            mNext.deliver(q);")
        sb.appendLine("        } else {")
        sb.appendLine("            finishInputEvent(q);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    protected void forward(QueuedInputEvent q) {")
        sb.appendLine("        if (mNext != null) {")
        sb.appendLine("            mNext.deliver(q);")
        sb.appendLine("        } else {")
        sb.appendLine("            finishInputEvent(q);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
