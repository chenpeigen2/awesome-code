        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 交给 InputStage 处理")
        sb.appendLine("    if (stage != null) {")
        sb.appendLine("        stage.deliver(q);")
        sb.appendLine("    } else {")
        sb.appendLine("        finishInputEvent(q);")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【finishInputEvent】")
        sb.appendLine("```java")
        sb.appendLine("private void finishInputEvent(QueuedInputEvent q) {")
        sb.appendLine("    Trace.asyncTraceEnd(...);")
        sb.appendLine("    ")
        sb.appendLine("    if (q.mReceiver != null) {")
        sb.appendLine("        // 通知 InputDispatcher 事件已处理完")
        sb.appendLine("        q.mReceiver.finishInputEvent(q.mEvent);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 回收事件")
        sb.appendLine("    recycleQueuedInputEvent(q);")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【事件处理结果】")
        sb.appendLine("```java")
        sb.appendLine("// InputStage 处理结果")
        sb.appendLine("public static final int FORWARD = 0;   // 继续传递给下一个 Stage")
        sb.appendLine("public static final int FINISH_HANDLED = 1;  // 已处理，完成")
        sb.appendLine("public static final int FINISH_NOT_HANDLED = 2;  // 未处理，完成")
        sb.appendLine("```\n")
        
        sb.appendLine("【处理流程】")
        sb.appendLine("```\n")
        sb.appendLine("deliverInputEvent()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("InputStage.deliver()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("onProcess() → 处理事件")
        sb.appendLine("       │")
        sb.appendLine("       ├─→ FORWARD: 传递给下一个 Stage")
        sb.appendLine("       │")
        sb.appendLine("       ├─→ FINISH_HANDLED: 调用 finishInputEvent()")
        sb.appendLine("       │")
        sb.appendLine("       └─→ FINISH_NOT_HANDLED: 调用 finishInputEvent()")
        sb.appendLine("```")
        
        binding.tvContent.text = sb.toString()
    }

