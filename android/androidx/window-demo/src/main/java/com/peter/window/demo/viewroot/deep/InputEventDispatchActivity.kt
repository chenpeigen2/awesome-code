        sb.clear()
        sb.appendLine("=== enqueueInputEvent ===\n")
        
        sb.appendLine("【方法签名】")
        sb.appendLine("```java")
        sb.appendLine("void enqueueInputEvent(InputEvent event,")
        sb.appendLine("        InputEventReceiver receiver, int flags, boolean processImmediately) {")
        sb.appendLine("    // 1. 创建 QueuedInputEvent")
        sb.appendLine("    QueuedInputEvent q = obtainQueuedInputEvent(event, receiver, flags);")
        sb.appendLine("    ")
        sb.appendLine("    // 2. 添加到队列")
        sb.appendLine("    QueuedInputEvent last = mPendingInputEventTail;")
        sb.appendLine("    if (last == null) {")
        sb.appendLine("        mPendingInputEventHead = q;")
        sb.appendLine("        mPendingInputEventTail = q;")
        sb.appendLine("    } else {")
        sb.appendLine("        last.mNext = q;")
        sb.appendLine("        mPendingInputEventTail = q;")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 3. 立即处理或稍后处理")
        sb.appendLine("    if (processImmediately) {")
        sb.appendLine("        doProcessInputEvents();")
        sb.appendLine("    } else {")
        sb.appendLine("        scheduleProcessInputEvents();")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【QueuedInputEvent】")
        sb.appendLine("```java")
        sb.appendLine("final class QueuedInputEvent {")
        sb.appendLine("    InputEvent mEvent;              // 原始事件")
        sb.appendLine("    InputEventReceiver mReceiver;   // 事件接收器")
        sb.appendLine("    int mFlags;                     // 标志")
        sb.appendLine("    QueuedInputEvent mNext;         // 链表下一个")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【doProcessInputEvents】")
        sb.appendLine("```java")
        sb.appendLine("void doProcessInputEvents() {")
        sb.appendLine("    // 遍历事件队列")
        sb.appendLine("    while (mPendingInputEventHead != null) {")
        sb.appendLine("        QueuedInputEvent q = mPendingInputEventHead;")
        sb.appendLine("        mPendingInputEventHead = q.mNext;")
        sb.appendLine("        ")
        sb.appendLine("        if (mPendingInputEventHead == null) {")
        sb.appendLine("            mPendingInputEventTail = null;")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        q.mFlags |= QueuedInputEvent.FLAG_DEFERRED;")
        sb.appendLine("        ")
        sb.appendLine("        // 分发事件")
        sb.appendLine("        deliverInputEvent(q);")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【事件队列模型】")
