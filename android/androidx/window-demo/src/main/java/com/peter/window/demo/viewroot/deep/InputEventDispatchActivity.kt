        sb.appendLine("```java")
        sb.appendLine("public boolean dispatchTouchEvent(MotionEvent ev) {")
        sb.appendLine("    // 检查是否被拦截")
        sb.appendLine("    final boolean intercepted;")
        sb.appendLine("    if (actionMasked == MotionEvent.ACTION_DOWN || mFirstTouchTarget != null) {")
        sb.appendLine("        final boolean disallowIntercept = (mGroupFlags & FLAG_DISALLOW_INTERCEPT) != 0;")
        sb.appendLine("        if (!disallowIntercept) {")
        sb.appendLine("            intercepted = onInterceptTouchEvent(ev);")
        sb.appendLine("        } else {")
        sb.appendLine("            intercepted = false;")
        sb.appendLine("        }")
        sb.appendLine("    } else {")
        sb.appendLine("        intercepted = true;")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 如果没被拦截，分发给子 View")
        sb.appendLine("    if (!intercepted) {")
        sb.appendLine("        for (int i = childrenCount - 1; i >= 0; i--) {")
        sb.appendLine("            View child = children[i];")
        sb.appendLine("            ")
        sb.appendLine("            // 检查事件是否在子 View 内")
        sb.appendLine("            if (!isTransformedTouchPointInView(x, y, child, null)) {")
        sb.appendLine("                continue;")
        sb.appendLine("            }")
        sb.appendLine("            ")
        sb.appendLine("            // 分发给子 View")
        sb.appendLine("            if (dispatchTransformedTouchEvent(ev, child)) {")
        sb.appendLine("                // 子 View 消费了事件")
        sb.appendLine("                newTouchTarget = addTouchTarget(child, idBitToAssign);")
        sb.appendLine("                break;")
        sb.appendLine("            }")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 处理触摸目标")
        sb.appendLine("    if (mFirstTouchTarget == null) {")
        sb.appendLine("        // 没有子 View 消费，自己处理")
        sb.appendLine("        dispatchTransformedTouchEvent(ev, null);")
        sb.appendLine("    } else {")
        sb.appendLine("        // 分发给触摸目标")
        sb.appendLine("        TouchTarget target = mFirstTouchTarget;")
        sb.appendLine("        while (target != null) {")
        sb.appendLine("            dispatchTransformedTouchEvent(ev, target.child);")
        sb.appendLine("            target = target.next;")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("    return handled;")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
