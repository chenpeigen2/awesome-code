    /**
     * 绘制流程详解
     */
    private fun showDrawProcess() {
        sb.clear()
        sb.appendLine("=== 绘制流程 (Draw) ===\n")
        
        sb.appendLine("【performDraw】")
        sb.appendLine("```java")
        sb.appendLine("private void performDraw() {")
        sb.appendLine("    if (mAttachInfo.mTreeObserver.hasOnPreDrawListeners()) {")
        sb.appendLine("        mAttachInfo.mTreeObserver.dispatchOnPreDraw();")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    try {")
        sb.appendLine("        draw(fullRedrawNeeded);")
        sb.appendLine("    } finally {")
        sb.appendLine("        mIsDrawing = false;")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【draw 方法】")
        sb.appendLine("```java")
        sb.appendLine("private boolean draw(boolean fullRedrawNeeded) {")
        sb.appendLine("    final Surface surface = mSurface;")
        sb.appendLine("    ")
        sb.appendLine("    // 检查 Surface 是否有效")
        sb.appendLine("    if (!surface.isValid()) {")
        sb.appendLine("        return false;")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 如果使用硬件加速")
        sb.appendLine("    if (mHardwareAccelerated && mThreadedRenderer != null) {")
        sb.appendLine("        mThreadedRenderer.draw(mView, mAttachInfo, this);")
        sb.appendLine("    } else {")
        sb.appendLine("        // 软件绘制")
        sb.appendLine("        if (!drawSoftware(surface, mAttachInfo, xOffset, yOffset,")
        sb.appendLine("                dirtyNeeded, new Rect())) {")
        sb.appendLine("            return false;")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("    return true;")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【drawSoftware - 软件绘制】")
        sb.appendLine("```java")
        sb.appendLine("private boolean drawSoftware(Surface surface, AttachInfo attachInfo,")
        sb.appendLine("        int xoff, int yoff, boolean scalingRequired, Rect dirty) {")
        sb.appendLine("    final Canvas canvas;")
        sb.appendLine("    ")
        sb.appendLine("    try {")
        sb.appendLine("        // 1. 锁定 Canvas")
        sb.appendLine("        canvas = mSurface.lockCanvas(dirty);")
        sb.appendLine("        ")
        sb.appendLine("        // 2. 如果需要，设置缩放")
        sb.appendLine("        if (scalingRequired) {")
        sb.appendLine("            canvas.scale(...);")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        // 3. 设置密度")
        sb.appendLine("        canvas.setDensity(mDensity);")
        sb.appendLine("        ")
        sb.appendLine("        try {")
        sb.appendLine("            // 4. 调用 View.draw")
        sb.appendLine("            mView.draw(canvas);")
        sb.appendLine("        } finally {")
        sb.appendLine("            // 5. 解锁并提交")
        sb.appendLine("            surface.unlockCanvasAndPost(canvas);")
        sb.appendLine("        }")
        sb.appendLine("    } catch (Exception e) {")
        sb.appendLine("        return false;")
        sb.appendLine("    }")
        sb.appendLine("    return true;")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【View.draw - 绘制的 6 个步骤】")
        sb.appendLine("```java")
        sb.appendLine("public void draw(Canvas canvas) {")
        sb.appendLine("    final int privateFlags = mPrivateFlags;")
        sb.appendLine("    final boolean dirtyOpaque = (privateFlags & PFLAG_DIRTY_MASK) == PFLAG_DIRTY_OPAQUE;")
        sb.appendLine("    ")
        sb.appendLine("    /*")
        sb.appendLine("     * Draw traversal performs several drawing steps which must be executed")
        sb.appendLine("     * in the appropriate order:")
        sb.appendLine("     *")
        sb.appendLine("     *      1. Draw the background")
        sb.appendLine("     *      2. If necessary, save the canvas' layers to prepare for fading")
        sb.appendLine("     *      3. Draw view's content")
        sb.appendLine("     *      4. Draw children")
        sb.appendLine("     *      5. If necessary, draw the fading edges and restore layers")
        sb.appendLine("     *      6. Draw decorations (scrollbars for instance)")
        sb.appendLine("     */")
        sb.appendLine("    ")
        sb.appendLine("    // Step 1: 绘制背景")
        sb.appendLine("    if (!dirtyOpaque) {")
        sb.appendLine("        drawBackground(canvas);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // Step 2: 保存 Canvas 图层（用于淡入淡出效果）")
        sb.appendLine("    int saveCount = 0;")
        sb.appendLine("    if (!dirtyOpaque && ... fadingEdgeRequired) {")
        sb.appendLine("        saveCount = canvas.save();")
        sb.appendLine("        // ...")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // Step 3: 绘制内容")
        sb.appendLine("    if (!dirtyOpaque) {")
        sb.appendLine("        onDraw(canvas);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // Step 4: 绘制子 View")
