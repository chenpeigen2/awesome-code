    /**
     * 测量流程详解
     */
    private fun showMeasureProcess() {
        sb.clear()
        sb.appendLine("=== 测量流程 (Measure) ===\n")
        
        sb.appendLine("【performMeasure】")
        sb.appendLine("```java")
        sb.appendLine("private void performMeasure(int widthMeasureSpec, int heightMeasureSpec) {")
        sb.appendLine("    Trace.traceBegin(Trace.TRACE_TAG_VIEW, \"measure\");")
        sb.appendLine("    try {")
        sb.appendLine("        mView.measure(widthMeasureSpec, heightMeasureSpec);")
        sb.appendLine("    } finally {")
        sb.appendLine("        Trace.traceEnd(Trace.TRACE_TAG_VIEW);")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【View.measure】")
        sb.appendLine("```java")
        sb.appendLine("public final void measure(int widthMeasureSpec, int heightMeasureSpec) {")
        sb.appendLine("    // 检查是否需要强制测量或尺寸变化")
        sb.appendLine("    boolean forceLayout = (mPrivateFlags & PFLAG_FORCE_LAYOUT) == PFLAG_FORCE_LAYOUT;")
        sb.appendLine("    ")
        sb.appendLine("    // 检查 MeasureSpec 是否变化")
        sb.appendLine("    final boolean specChanged = widthMeasureSpec != mOldWidthMeasureSpec")
        sb.appendLine("            || heightMeasureSpec != mOldHeightMeasureSpec;")
        sb.appendLine("    final boolean isSpecExactly = MeasureSpec.getMode(widthMeasureSpec) == MeasureSpec.EXACTLY")
        sb.appendLine("            && MeasureSpec.getMode(heightMeasureSpec) == MeasureSpec.EXACTLY;")
        sb.appendLine("    final boolean matchesSpecSize = getMeasuredWidth() == MeasureSpec.getSize(widthMeasureSpec)")
        sb.appendLine("            && getMeasuredHeight() == MeasureSpec.getSize(heightMeasureSpec);")
        sb.appendLine("    final boolean needsLayout = specChanged && (sAlwaysRemeasureExactly || !isSpecExactly")
        sb.appendLine("            || !matchesSpecSize);")
        sb.appendLine("    ")
        sb.appendLine("    if (forceLayout || needsLayout) {")
        sb.appendLine("        // 清除测量尺寸")
        sb.appendLine("        mPrivateFlags &= ~PFLAG_MEASURED_DIMENSION_SET;")
        sb.appendLine("        ")
        sb.appendLine("        // 开始测量")
        sb.appendLine("        onMeasure(widthMeasureSpec, heightMeasureSpec);")
        sb.appendLine("        ")
        sb.appendLine("        // 检查是否调用了 setMeasuredDimension")
        sb.appendLine("        if ((mPrivateFlags & PFLAG_MEASURED_DIMENSION_SET) != PFLAG_MEASURED_DIMENSION_SET) {")
        sb.appendLine("            throw new IllegalStateException(\"View with id \" + getId() +")
        sb.appendLine("                \": onMeasure() did not set the measured dimension\");")
        sb.appendLine("        }")
        sb.appendLine("        ")
        sb.appendLine("        // 标记已测量")
        sb.appendLine("        mPrivateFlags |= PFLAG_LAYOUT_REQUIRED;")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    mOldWidthMeasureSpec = widthMeasureSpec;")
        sb.appendLine("    mOldHeightMeasureSpec = heightMeasureSpec;")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【View.onMeasure】")
        sb.appendLine("```java")
        sb.appendLine("protected void onMeasure(int widthMeasureSpec, int heightMeasureSpec) {")
        sb.appendLine("    // 默认实现：使用背景最小尺寸或建议尺寸")
        sb.appendLine("    setMeasuredDimension(")
        sb.appendLine("        getDefaultSize(getSuggestedMinimumWidth(), widthMeasureSpec),")
        sb.appendLine("        getDefaultSize(getSuggestedMinimumHeight(), heightMeasureSpec)")
        sb.appendLine("    );")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【ViewGroup.onMeasure】")
        sb.appendLine("```java")
        sb.appendLine("// ViewGroup 需要测量所有子 View")
        sb.appendLine("protected void onMeasure(int widthMeasureSpec, int heightMeasureSpec) {")
        sb.appendLine("    // 遍历子 View")
        sb.appendLine("    final int childCount = getChildCount();")
        sb.appendLine("    for (int i = 0; i < childCount; i++) {")
        sb.appendLine("        final View child = getChildAt(i);")
        sb.appendLine("        ")
        sb.appendLine("        // 测量子 View")
        sb.appendLine("        measureChild(child, widthMeasureSpec, heightMeasureSpec);")
        sb.appendLine("        // 或者")
        sb.appendLine("        measureChildWithMargins(child, widthMeasureSpec, ...);")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    // 设置自己的尺寸")
        sb.appendLine("    setMeasuredDimension(width, height);")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【measureChild】")
        sb.appendLine("```java")
        sb.appendLine("protected void measureChild(View child, int parentWidthMeasureSpec,")
        sb.appendLine("        int parentHeightMeasureSpec) {")
        sb.appendLine("    final LayoutParams lp = child.getLayoutParams();")
        sb.appendLine("    ")
        sb.appendLine("    // 根据父容器的 MeasureSpec 和子 View 的 LayoutParams")
        sb.appendLine("    // 计算子 View 的 MeasureSpec")
        sb.appendLine("    final int childWidthMeasureSpec = getChildMeasureSpec(")
        sb.appendLine("            parentWidthMeasureSpec, mPaddingLeft + mPaddingRight, lp.width);")
        sb.appendLine("    final int childHeightMeasureSpec = getChildMeasureSpec(")
        sb.appendLine("            parentHeightMeasureSpec, mPaddingTop + mPaddingBottom, lp.height);")
        sb.appendLine("    ")
        sb.appendLine("    // 测量子 View")
        sb.appendLine("    child.measure(childWidthMeasureSpec, childHeightMeasureSpec);")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【测量流程图】")
        sb.appendLine("```\n")
        sb.appendLine("performMeasure()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("DecorView.measure()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("DecorView.onMeasure()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("遍历子 View → measureChild()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("child.measure()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("child.onMeasure()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("setMeasuredDimension()")
        sb.appendLine("```")
        
        binding.tvContent.text = sb.toString()
    }

