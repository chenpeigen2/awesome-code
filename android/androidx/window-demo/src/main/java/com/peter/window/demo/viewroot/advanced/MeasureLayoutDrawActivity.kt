        sb.appendLine("    // 检查尺寸是否变化")
        sb.appendLine("    boolean changed = isLayoutModeOptical(mParent) ?")
        sb.appendLine("            setOpticalFrame(l, t, r, b) : setFrame(l, t, r, b);")
        sb.appendLine("    ")
        sb.appendLine("    if (changed || (mPrivateFlags & PFLAG_LAYOUT_REQUIRED) == PFLAG_LAYOUT_REQUIRED) {")
        sb.appendLine("        // 调用 onLayout")
        sb.appendLine("        onLayout(changed, l, t, r, b);")
        sb.appendLine("        ")
        sb.appendLine("        // 清除布局请求标志")
        sb.appendLine("        mPrivateFlags &= ~PFLAG_LAYOUT_REQUIRED;")
        sb.appendLine("        ")
        sb.appendLine("        // 派发布局变化事件")
        sb.appendLine("        ListenerInfo li = mListenerInfo;")
        sb.appendLine("        if (li != null && li.mOnLayoutChangeListeners != null) {")
        sb.appendLine("            for (OnLayoutChangeListener listener : li.mOnLayoutChangeListeners) {")
        sb.appendLine("                listener.onLayoutChange(this, l, t, r, b, oldL, oldT, oldR, oldB);")
        sb.appendLine("            }")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("    ")
        sb.appendLine("    mPrivateFlags &= ~PFLAG_FORCE_LAYOUT;")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【setFrame - 保存位置信息】")
        sb.appendLine("```java")
        sb.appendLine("protected boolean setFrame(int left, int top, int right, int bottom) {")
        sb.appendLine("    boolean changed = false;")
        sb.appendLine("    ")
        sb.appendLine("    if (mLeft != left || mRight != right")
        sb.appendLine("            || mTop != top || mBottom != bottom) {")
        sb.appendLine("        changed = true;")
        sb.appendLine("        ")
        sb.appendLine("        // 保存位置")
        sb.appendLine("        mLeft = left;")
        sb.appendLine("        mTop = top;")
        sb.appendLine("        mRight = right;")
        sb.appendLine("        mBottom = bottom;")
        sb.appendLine("        ")
        sb.appendLine("        // 计算尺寸")
        sb.appendLine("        mWidth = right - left;")
        sb.appendLine("        mHeight = bottom - top;")
        sb.appendLine("        ")
        sb.appendLine("        // 标记需要重绘")
        sb.appendLine("        mPrivateFlags |= PFLAG_DRAWN;")
        sb.appendLine("    }")
        sb.appendLine("    return changed;")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【ViewGroup.onLayout】")
        sb.appendLine("```java")
        sb.appendLine("// ViewGroup.onLayout 是抽象方法，子类必须实现")
        sb.appendLine("protected abstract void onLayout(boolean changed,")
        sb.appendLine("        int l, int t, int r, int b);")
        sb.appendLine("")
        sb.appendLine("// 例如 LinearLayout 的实现")
        sb.appendLine("protected void onLayout(boolean changed, int l, int t, int r, int b) {")
        sb.appendLine("    if (mOrientation == VERTICAL) {")
        sb.appendLine("        layoutVertical(l, t, r, b);")
        sb.appendLine("    } else {")
        sb.appendLine("        layoutHorizontal(l, t, r, b);")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("")
        sb.appendLine("void layoutVertical(int left, int top, int right, int bottom) {")
        sb.appendLine("    int childTop = top + mPaddingTop;")
        sb.appendLine("    int childLeft = left + mPaddingLeft;")
        sb.appendLine("    ")
        sb.appendLine("    final int count = getChildCount();")
        sb.appendLine("    for (int i = 0; i < count; i++) {")
        sb.appendLine("        final View child = getChildAt(i);")
        sb.appendLine("        if (child.getVisibility() != View.GONE) {")
        sb.appendLine("            // 布局子 View")
        sb.appendLine("            child.layout(childLeft, childTop,")
        sb.appendLine("                    childLeft + child.getMeasuredWidth(),")
        sb.appendLine("                    childTop + child.getMeasuredHeight());")
        sb.appendLine("            ")
        sb.appendLine("            childTop += child.getMeasuredHeight();")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
        sb.appendLine("```\n")
        
        sb.appendLine("【布局流程图】")
        sb.appendLine("```\n")
        sb.appendLine("performLayout()")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("DecorView.layout(0, 0, width, height)")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("setFrame() → 保存 mLeft, mTop, mRight, mBottom")
        sb.appendLine("       │")
        sb.appendLine("       ↓")
        sb.appendLine("DecorView.onLayout()")
        sb.appendLine("       │")
