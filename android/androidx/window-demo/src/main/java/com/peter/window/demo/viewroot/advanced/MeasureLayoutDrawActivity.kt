package com.peter.window.demo.viewroot.advanced

import android.os.Bundle
import androidx.appcompat.app.AppCompatActivity
import com.peter.window.demo.databinding.ActivityMeasureLayoutDrawBinding

/**
 * 进阶篇：测量、布局、绘制流程详解
 * 
 * performTraversals() 是 ViewRootImpl 最核心的方法，
 * 它负责执行 View 树的测量、布局、绘制三个步骤。
 * 
 * 流程图：
 * performTraversals()
 *     ├── performMeasure() → measure() → onMeasure()
 *     ├── performLayout()  → layout()  → onLayout()
 *     └── performDraw()    → draw()    → onDraw()
 */
class MeasureLayoutDrawActivity : AppCompatActivity() {

    private lateinit var binding: ActivityMeasureLayoutDrawBinding
    private val sb = StringBuilder()

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        binding = ActivityMeasureLayoutDrawBinding.inflate(layoutInflater)
        setContentView(binding.root)
        supportActionBar?.setDisplayHomeAsUpEnabled(true)

        setupListeners()
        showPerformTraversals()
    }

    private fun setupListeners() {
        binding.btnTraversals.setOnClickListener { showPerformTraversals() }
        binding.btnMeasure.setOnClickListener { showMeasureProcess() }
        binding.btnLayout.setOnClickListener { showLayoutProcess() }
        binding.btnDraw.setOnClickListener { showDrawProcess() }
        binding.btnMeasureSpec.setOnClickListener { showMeasureSpec() }
        binding.btnForceLayout.setOnClickListener { showForceLayoutProcess() }
        binding.btnInvalidate.setOnClickListener { showInvalidateProcess() }
        binding.btnRequestLayout.setOnClickListener { showRequestLayoutProcess() }
    }

