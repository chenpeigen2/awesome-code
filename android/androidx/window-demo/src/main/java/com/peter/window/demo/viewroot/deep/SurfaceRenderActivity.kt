        sb.appendLine("【lockCanvas Native 实现】")
        sb.appendLine("```cpp")
        sb.appendLine("// Surface::lockCanvas 最终调用")
        sb.appendLine("status_t Surface::lock(BufferBase* buffer, bool blocking) {")
        sb.appendLine("    // 1. 从 BufferQueue 获取一个空闲缓冲区")
        sb.appendLine("    status_t err = dequeueBuffer(&buf, &fenceFd);")
        sb.appendLine("    ")
        sb.appendLine("    // 2. 等待缓冲区可用")
        sb.appendLine("    waitFence(fenceFd);")
        sb.appendLine("    ")
        sb.appendLine("    // 3. 锁定缓冲区，获取内存地址")
        sb.appendLine("    void* vaddr;")
        sb.appendLine("    buf->lock(GRALLOC_USAGE_SW_WRITE_OFTEN, &vaddr);")
        sb.appendLine("    ")
        sb.appendLine("    // 4. 创建 Skia Canvas")
        sb.appendLine("    mLockedBuffer = buf;")
        sb.appendLine("    return NO_ERROR;")
        sb.appendLine("}")
        sb.appendLine("```\n")

        sb.appendLine("【unlockCanvasAndPost 实现】")
        sb.appendLine("```cpp")
        sb.appendLine("status_t Surface::unlockAndPost() {")
        sb.appendLine("    // 1. 解锁缓冲区")
        sb.appendLine("    mLockedBuffer->unlock();")
        sb.appendLine("    ")
        sb.appendLine("    // 2. 将缓冲区入队到 BufferQueue")
        sb.appendLine("    // SurfaceFlinger 会从这里取走并合成")
        sb.appendLine("    status_t err = queueBuffer(mLockedBuffer, ...);")
        sb.appendLine("    ")
        sb.appendLine("    // 3. 通知 SurfaceFlinger 有新帧")
        sb.appendLine("    mGraphicBufferProducer->queueBuffer(...);")
        sb.appendLine("    ")
        sb.appendLine("    mPostedBuffer = mLockedBuffer;")
        sb.appendLine("    mLockedBuffer = 0;")
        sb.appendLine("    return err;")
        sb.appendLine("}")
        sb.appendLine("```")

        binding.tvContent.text = sb.toString()
    }

