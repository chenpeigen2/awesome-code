        sb.appendLine("    // 1. 锁定 Surface，获取 Canvas")
        sb.appendLine("    Rect dirty = mDirty;")
        sb.appendLine("    Canvas canvas = mSurface.lockCanvas(dirty);")
        sb.appendLine("    ")
        sb.appendLine("    if (canvas != null) {")
        sb.appendLine("        try {")
        sb.appendLine("            // 2. 在 Canvas 上绘制")
        sb.appendLine("            canvas.drawColor(Color.WHITE);  // 清空背景")
        sb.appendLine("            mView.draw(canvas);  // 绘制 View 树")
        sb.appendLine("            ")
        sb.appendLine("        } finally {")
        sb.appendLine("            // 3. 解锁并提交")
        sb.appendLine("            mSurface.unlockCanvasAndPost(canvas);")
        sb.appendLine("        }")
        sb.appendLine("    }")
        sb.appendLine("}")
