    /**
     * 绘制线程
     */
    private inner class DrawThread(private val surface: Surface) : Thread() {
        private var running = true
        private var frameCount = 0
        private val paint = Paint()

        override fun run() {
            paint.color = Color.BLUE
            paint.textSize = 40f
            paint.isAntiAlias = true

            while (running) {
                var canvas: Canvas? = null
                try {
                    canvas = surface.lockCanvas(null)
                    if (canvas != null) {
                        // 清空背景
                        canvas.drawColor(Color.WHITE)

                        // 绘制帧计数
                        canvas.drawText("Frame: $frameCount", 50f, 80f, paint)

                        // 绘制一个简单的动画
                        val x = (frameCount % 200).toFloat()
                        paint.color = Color.RED
                        canvas.drawCircle(x + 50, 120f, 30f, paint)

                        frameCount++
                    }
                } finally {
                    if (canvas != null) {
                        try {
                            surface.unlockCanvasAndPost(canvas)
                        } catch (e: Exception) {
                            // Surface 可能已销毁
                        }
                    }
                }

                try {
                    Thread.sleep(16) // 约 60fps
                } catch (e: InterruptedException) {
                    break
                }
            }
        }

        fun stopDrawing() {
            running = false
            interrupt()
        }
    }

